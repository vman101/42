#include "libft.h"

char *ft_strrchr(char *s, int c)
{
	char *n;
	int i;

	i = 0;
	n = NULL;
	while (s[i] != 0)
	{
		if (s[i] == c)
			n = &s[i];
		i++;
	}
	if (c == 0)
		return (&s[i]);
	if (n)
		return (n);
	return (NULL);
}
