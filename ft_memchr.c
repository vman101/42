#include "libft.h"

void	*ft_memchr(void *s, int c, size_t n)
{
	char	*p;
	size_t	i;

	i = 0;
	p = (char *)s;
	while (i < n)
	{
		if (p[i] == c)
			return (&p[i]);
		i++;
	}
	if (p[i - 1] == c)
		return (&p[i]);
	return (NULL);
}
